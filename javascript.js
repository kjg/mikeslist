//Filter Buttons
function showAll() {
  // select all elements with the "product" class
  const products = document.querySelectorAll(".product");

  // iterate over the selected elements and set their display property to none
  for (let i = 0; i < products.length; i++) {
    products[i].style.opacity = "1";
    products[i].style.display = "inline-block";
  }
}

function edcFilter() {
  // select all elements with the "product" class
  const products = document.querySelectorAll(".product");
  // iterate over the selected elements and set their display property to none
  for (let i = 0; i < products.length; i++) {
    products[i].style.display = "none";
  }
  $("span.edc").parent().css("display", "block");
}

//SETTINGS PAGE

// First, select the element with the class "settings"
const settingsDiv = document.querySelector(".settings");

// This is the onclick function that will be called when the element is clicked
function showSettings() {
  // Set the opacity of the element to 1 and the z-index to 2
  settingsDiv.setAttribute("style", "opacity: 1; z-index: 2;");
}

function hideSettings() {
  // Set the opacity of the element to 1 and the z-index to 2
  settingsDiv.setAttribute("style", "opacity: 0; z-index: 0;");
}
